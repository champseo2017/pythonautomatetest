"""
เมธอด isdigit() ถ้าสตริงเป็นตัวเลขทั้งหมด ผลลัพธ์ที่ได้จะเป็นจริง (True)
แต่ถ้าไม่ใช้ตัวเลขทั้งหมดผลลัพธ์ที่ได้จะเป็น (false)
"""
str1 = "Python3.6.1"
str2 = "0899612358"
str3 = "Python361"
print("str1 เป็นตัวเลขทั้งหมดหรือไม่ =", str1.isdigit())
print("str2 เป็นตัวเลขทั้งหมดหรือไม่ =", str2.isdigit())
print("str3 เป็นตัวเลขทั้งหมดหรือไม่ =", str3.isdigit())