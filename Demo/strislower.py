"""
เมธอด islower() ถ้าสตริงเป็นตัวอักษรพิมพ์เล็ก ผลลัพธ์ที่ได้จะเป็นจริง (True)
แต่ถ้าไม่ใช้ตัวอักษรพิมพ์เล็กทั้งหมดผลลัพธ์ที่ได้จะเเป็น (False)
"""
