# การใช้เครื่องหมาย (), [], {} ร่วมกับตัวแปรหรือฟังก์ชัน ส่วนใหญ่ถ้ามีการใช้เครื่องหมายร่วมกับตัวแปร
# ลักษณะของตัวแปรเหล่านั้นจะเป็นประเภท Sequennce ( คล้ายกับตัวแปร array ในภาษษอื่น )
a = (1, 2, 3, 4) # Tuple จะใช้เครื่องหมาย ()
b = ['TV', 'หลอดไฟ', 'ปลาทู'] # List จะใช้เครื่องหมาย []
c = {100: 'TV', 200: 'หลอดไฟ', 300: 'ปลาทู'} # Dictionary จะใช้เครื่องหมาย {}
# การอ่านค่าในตัวแปร Sequence
print(a[0], a[1], a[2], a[3])