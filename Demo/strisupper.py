"""
เมธอด isupper () ใช้ตรวจสอลสตริงเป็นตัวอักษรพิมพ์ใหญ่ทั้งหมดหรือไม่ ผลลัพธ์ที่ได้จะเป็นจริง (True)
ถ้าไม่ใช้ตัวอักษรพิมพ์ใหญ่ทั้งหมดผลลัพธ์ที่ได้จะเป็นเท็จ (False)
"""
str1 = "PYTHON PROGARMMING"
str2 = "Python Programming"
print("str1 เป็นตัวอักษรพิมพ์ใหญ่ทั้งหมดหรือไม่ =", str1.isupper())
print("str2 เป็นตัวอักษรพิมพ์ใหญ่ทั้งหมดหรือไม่ =", str2.isupper())