# ข้อมูลชนิดทูเพิลไม่สามารถทำการเปลียนแปลงได้
books_lst_lst = ["Python", "Java", "C", "C++", "c#"]
numbers_lst = [1, 2, 3, 4, 5, 6]
book_num_lst = [1, "Python", 2, "Java", 3, "C", 4, "C++", 5, "c#"]
print("รายชื่อหนังสือใน books_lst = ", books_lst_lst)
print("ตัวเลขใน numbers_lst = ", numbers_lst)
print("สมาชิกใน book_num_lst = ", book_num_lst)