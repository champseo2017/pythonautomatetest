"""
เมธอด isspace() ถ้าสตริงเป็ฯค่าว่าง ผลลัพธ์ที่ได้จะเป็นจริง (True)
แต่ถ้าไม่ใช้ผลลัพธ์ที่ได้จะเป็นเท็จ (False)
"""
str1 = " "
str2 = "Python Programming"
str3 = ""
print("str1 เป็นค่าว่างหรือไม่ =", str1.isspace())
print("str2 เป็นค่าว่างหรือไม่ =", str2.isspace())
print("str3 เป็นค่าว่างหรือไม่ =", str3.isspace())