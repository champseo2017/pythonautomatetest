# เมธอดที่ใช้กับข้อมูลชนิด list ( ลิสต์ )
# เมธอด append() ใช้สำหรับเพิ่มข้อมูลต่อท้ายลิสต์
# โดยที่ lst คือ ตัวแปรชนิดข้อมูลลิสต์
#     x คือ ข้อมูลใหม่ที่ต้องการเพิ่ม
# lst.append(x)
books_lst = ['Python', 'Java', 'C', 'C++', 'C#']
books_lst.append("Scala")
print("แสดงรายการหนังสือใน books_lst ", books_lst)